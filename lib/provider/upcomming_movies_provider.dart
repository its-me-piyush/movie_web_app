class UpcommingMoviesProvider with ChangeNotifier {
  Future<List<UpcommingMoviesResults>> fetchNowPlayingMovies() async {
    try {
      var url = Uri.parse(
          '$baseUrl/movie/upcoming?api_key=$apiKey&language=en-US&page=1');

      var response = await http.get(url);
      var results = upcommingMoviesFromJson(response.body);
      return results.results;
    } catch (e) {
      rethrow;
    }
  }
}
