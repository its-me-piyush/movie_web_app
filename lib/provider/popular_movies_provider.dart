class PopularMoviesProvider with ChangeNotifier {
  Future<List<PopularMoviesResults>> fetchNowPlayingMovies() async {
    try {
      var url = Uri.parse(
          '$baseUrl/movie/popular?api_key=$apiKey&language=en-US&page=1');

      var response = await http.get(url);
      var results = popularMovieFromJson(response.body);
      return results.results;
    } catch (e) {
      rethrow;
    }
  }
}
